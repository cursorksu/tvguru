                        <h7 itemprop="description">HBO выпустил более дюжины кадров из седьмого сезона, и они завораживают</h7>
                        <p>Телеканал дает знать, что «Игра престолов» не пропал навсегда — сериал просто выйдет чуть позже. Ни для кого не секрет, что масштаб шоу только растет с каждым сезоном. Создатели уверяют нас, что CGI в 7 сезоне будет еще лучше, а действия еще масштабнее.</p>
                        <p>Напомним, что зима наконец наступила, и теперь действия шоу будут стремиться к высоченной кульминации, которая протянется и до финального восьмого сезона. Борьба за трон, война и драконы — главные достопримечательности этой главы.</p>
                        <p>Разумеется, на этих кадрах ничего сверхъестественного нет, но вон королева Серсея с новенькой короной, а тут — Сэм со своей одичалой знания добывают, Бран есть, Джон милашка, Санса с Мизинцем и Арья потеряшка, а Дэйенерис так вообще! — с новой брошью в виде дракончиков из <a href="#">тизера</a>. А шестой кадр непростой, о нем мы знаем кое-что <a href="#">спойлерное</a>.</p>
                        <p>Седьмой сезон выйдет на HBO и в Амедиатеке 17 июля. Ждите леденящее лето.</p>
