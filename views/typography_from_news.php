                      <div class="article-body">
                        <div class="body">
                    
                        <p>Наш Сайт, как и&nbsp;большинство других, использует файлы cookie и&nbsp;иные аналогичные технологии, чтобы предоставлять услуги, наиболее отвечающие вашим интересам и&nbsp;потребностям, а&nbsp;также собирать статистическую и&nbsp;маркетинговую информацию для анализа и&nbsp;совершенствования Сайта.</p>
                    <p>При использовании данного Сайта, вы&nbsp;подтверждаете свое согласие на&nbsp;использование файлов cookie и&nbsp;других похожих технологий в&nbsp;соответствии с&nbsp;настоящим Положением.</p>
                    <p>Если вы&nbsp;не&nbsp;согласны, чтобы мы&nbsp;использовали данный тип файлов, Вы&nbsp;должны соответствующим образом установить настройки вашего браузера или не&nbsp;использовать наш Сайт.</p>
                    <p>Обращаем ваше внимание на&nbsp;то, что при блокировании или удалении cookie-файлов, мы&nbsp;не&nbsp;можем гарантировать корректную работу нашего Сайта в&nbsp;вашем браузере.</p>
                    <p>Cookie-файлы, которые мы&nbsp;используем, не&nbsp;содержат сведений, на&nbsp;основании которых можно вас идентифицировать.</p>

                    <h3>Что такое файл cookie?</h3>
                    <p><em>Я курсив курсивный закурсивленный</em> представляет собой небольшой текстовый файл, сохраняемый на&nbsp;вашем компьютере, смартфоне или другом устройстве, которое вы&nbsp;используете для посещения интернет-сайтов.</p>
                    <p><em>Файлы cookie</em> могут размещаться на&nbsp;вашем устройстве нами (&laquo;собственные&raquo; файлы cookie) или другими операторами (файлы cookie &laquo;третьих лиц&raquo;).</p>
                    <p>Мы&nbsp;используем два вида файлов cookie на&nbsp;Сайте: &laquo;cookie сессии&raquo; и&nbsp;&laquo;постоянные cookie&raquo;. Cookie сессии&nbsp;&mdash; это временные файлы, которые остаются на&nbsp;устройстве пока вы&nbsp;не&nbsp;покинете Сайт. Постоянные cookie остаются на&nbsp;устройстве в&nbsp;течение длительного времени или пока вы&nbsp;вручную не&nbsp;удалите&nbsp;их (как долго cookie останется на&nbsp;вашем устройстве будет зависеть от&nbsp;продолжительности или &laquo;времени жизни&raquo; конкретного файла и&nbsp;настройки вашего браузера).</p>

                    <h3>Типы файлов cookie и&nbsp;их&nbsp;предназначение</h3>
                    <p>Необходимые файлы нужны для обеспечения правильной работы Сайта, использования его функций. Отключение использования таких файлов приведет к&nbsp;падению производительности Сайта, невозможности использовать его компоненты и&nbsp;сервисы.</p>
                    <p>Файлы cookie, относящиеся к&nbsp;производительности, эффективности и&nbsp;аналитике данные файлы позволяют анализировать взаимодействие посетителей с&nbsp;Сайтом, оптимизировать содержание Сайта, измерять эффективность рекламных кампаний, предоставляя информацию о&nbsp;количестве посетителей Сайта, времени его использования, возникающих ошибках.</p>
